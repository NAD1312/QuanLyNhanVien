        String staff_age = txtstaff_age.getText();
        String staff_address = txtstaff_address.getText();
        String staff_sex = txtstaff_sex.getText();

        if (staff_id.isEmpty()) {
            JOptionPane.showMessageDialog(this, "staff_id is empty");
            return;
        }
        if (staff_id.length() != 4) {
            JOptionPane.showMessageDialog(this, "length of staff_id equals to 4");
            return;
        }
        if (staff_password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "staff_password is empty");
            return;
        }
        if (staff_fullname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "staff_fullname is empty");
            return;
        }
        if (staff_age.isEmpty()) {
            JOptionPane.showMessageDialog(this, "staff_age is empty");
            return;
        }
        if (staff_address.isEmpty()) {
            JOptionPane.showMessageDialog(this, "staff_address is empty");
            return;
        }
        if (staff_sex.isEmpty()) {
            JOptionPane.showMessageDialog(this, "staff_sex is empty");
            return;
        }
        if (!staff_id.isEmpty() && !salary_id.isEmpty() && !staff_password.isEmpty() && !staff_fullname.isEmpty() && !staff_age.isEmpty() && !staff_address.isEmpty() && !staff_sex.isEmpty()) {
            Byte age = null;
            try {
                age = Byte.parseByte(staff_age);
                Staff st = new Staff(staff_id, salary_id, staff_password, staff_fullname, age, staff_address, staff_sex);

                if (qlnvs.insert(st)) {
                    JOptionPane.showMessageDialog(this, "Success");
                    showStaff();
                }else{
                    JOptionPane.showMessageDialog(this, "fail");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "staff_age is not valid");
            }

        }
