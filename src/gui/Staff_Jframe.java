    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // TODO add your handling code here:
        Staff st = new Staff();
        st.setStaff_id(txtstaff_id.getText());
        st.setSalary_id(txtsalary_id.getText());
        st.setStaff_password(txtstaff_pasword.getText());
        st.setStaff_fullname(txtstaff_fullname.getText());
        st.setStaff_age(Byte.parseByte(txtstaff_age.getText()));
        st.setStaff_address(txtstaff_address.getText());
        st.setStaff_sex(txtstaff_sex.getText());
        if (qlnvs.update(st)) {
            JOptionPane.showMessageDialog(this, "Success");
        } else {
            JOptionPane.showMessageDialog(this, "Fail");
        }
        showStaff();
    }//GEN-LAST:event_btnupdateActionPerformed

