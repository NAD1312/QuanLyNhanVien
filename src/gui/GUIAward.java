    private void btninsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btninsertActionPerformed
        // TODO add your handling code here:
        String project_id = txtproject_id.getText();
        String staff_id = txtstaff_id.getText();
        String salary_award = txtsalary_award.getText();

        Vector<Project> vt = qlnvs.getAllPro();
        boolean flag = false;
        for (Project pj : vt) {

            if (project_id.equals(pj.getProject_id())) {
                flag = true;
            }
        }

        List<Staff> li = qlnvs.getDataofstaff();
        boolean flags = false;
        for (Staff st : li) {
            if (staff_id.equals(st.getStaff_id())) {
                flags = true;
            }
        }

        if (project_id.isEmpty()) {
            JOptionPane.showMessageDialog(this, "project_id is empty");
            return;
        }
        if (staff_id.isEmpty()) {
            JOptionPane.showMessageDialog(this, "staff_id is empty");
            return;
        }
        if (salary_award.isEmpty()) {
            JOptionPane.showMessageDialog(this, "salary_award is empty");
            return;
        }
        if (!flag) {
            JOptionPane.showMessageDialog(this, "project_id does not exist");
            return;
        }
        if (!flags) {
            JOptionPane.showMessageDialog(this, "staff_id does not existed");
            return;
        }
        if (flag && flags) {
            int salary = 0;
            try {
                salary = Integer.parseInt(salary_award);
                Award aw = new Award(project_id, staff_id, salary);
                if (qlnvs.insert(aw)) {
                    JOptionPane.showMessageDialog(this, "success");
                    showAward();
                } else {
                    JOptionPane.showMessageDialog(this, "fail");
                }
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "salary_award is not valid");
            }

        }
    }//GEN-LAST:event_btninsertActionPerformed

