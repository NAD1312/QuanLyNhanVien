    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // TODO add your handling code here:
        int salary = 0;
        try {
            salary = Integer.parseInt(txtsalary_award.getText());
            Award a = new Award();
            a.setProject_id(txtproject_id.getText());
            a.setStaff_id(txtstaff_id.getText());
            a.setSalary_award(salary);
            if (qlnvs.update(a)) {
                JOptionPane.showMessageDialog(this, "Success");
            } else {
                JOptionPane.showMessageDialog(this, "Fail");
            }
            showAward();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "salary_award is not valid");
        }

    }//GEN-LAST:event_btnupdateActionPerformed

