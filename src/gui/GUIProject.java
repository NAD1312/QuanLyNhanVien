    public boolean kiemtra(String x, int vitri) {
        for (int i = 0; i < vctData.size(); i++) {
            Vector temp = new Vector((Vector) vctData.elementAt(i));
            if (x.equals(temp.get(vitri))) {
                return true;
            }
        }
        return false;
    }

    public void xoaTxt() {
        txtProID.setText("");
        txtProName.setText("");
        txtProStart.setText("");
        txtProFinish.setText("");
        txtProStatus.setText("");
        txtProID.requestFocusInWindow();
    }

    public void hienthi(Project x) {
        Vector vctRow = new Vector();
        vctRow.add(x.getProject_id());
        vctRow.add(x.getProject_name());
        vctRow.add(x.getProject_start());
        vctRow.add(x.getProject_finish());
        vctRow.add(x.getProject_status());
        vctData.add(vctRow);
        ProjectTable.setModel(new DefaultTableModel(vctData, vctHeader));
    }

    public void them(Project x, int vitri) {
        Vector vctRow = new Vector();
        vctRow.add(x.getProject_id());
        vctRow.add(x.getProject_name());
        vctRow.add(x.getProject_start());
        vctRow.add(x.getProject_finish());
        vctRow.add(x.getProject_status());
        vctData.set(vitri, vctRow);
        ProjectTable.setModel(new DefaultTableModel(vctData, vctHeader));
    }

    public void xoa(int vitri) {
        vctData.removeElementAt(vitri);
        ProjectTable.setModel(new DefaultTableModel(vctData, vctHeader));
    }
    
    

