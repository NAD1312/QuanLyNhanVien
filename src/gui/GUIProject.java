    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String sua_pro_id = txtProID.getText().toString();
        while (sua_pro_id.length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Project ID is empty!");
            return;
        }
        int i = 0;
        boolean flag = false;
        int vitri = 0;
        for (i = 0; i < vctData.size(); i++) {
            Vector temp = new Vector((Vector) vctData.elementAt(i));
            if (sua_pro_id.equals(temp.get(0))) {
                flag = true;
                vitri = i;
                break;
            }
        }
        if (flag == true) {
            String sua_pro_name = new String();
            String sua_pro_start = new String();
            String sua_pro_finish = new String();
            sua_pro_name = txtProName.getText().toString();
            while (sua_pro_name.length() == 0) {
                JOptionPane.showMessageDialog(rootPane, "Project name is empty!");
                return;
            }
            sua_pro_start = txtProStart.getText().toString();
            while (sua_pro_start.length() == 0) {
                JOptionPane.showMessageDialog(rootPane, "Project start day is empty!");
                return;
            }
            sua_pro_finish = txtProFinish.getText().toString();
            while (sua_pro_finish.length() == 0) {
                JOptionPane.showMessageDialog(rootPane, "Project finish day is empty!");
                return;
            }
            byte x = Byte.valueOf(txtProStatus.getText().toString());
            while (x != 0 && x != 1) {
                JOptionPane.showMessageDialog(rootPane, "Wrong valid of Status! If finish insert 1 else insert 0!");
                return;
            }
            Project sua_pro = new Project(sua_pro_id, sua_pro_name, sua_pro_start, sua_pro_finish, x);
            int check = daoPro.ChangeProject(sua_pro);
            if (check == 1) {
                them(sua_pro, vitri);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Wrong valid of Project Start or Project Finish!");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Project ID is not exists!");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (txtProID.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Project ID is empty!");
            return;
        }
        if (txtProName.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Project name is empty!!");
            return;
        }
        if (txtProStart.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Project start day is empty!");
            return;
        }
        if (txtProFinish.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Project finish day is empty!");
            return;
        }
        if (txtProStatus.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Project Status is empty");
            return;
        }
        if (txtProStatus.getText().equals("0") || txtProStatus.getText().equals("1")) {
            if (kiemtra(txtProID.getText(), 0)) {
                JOptionPane.showMessageDialog(rootPane, "Project ID already exists, please insert other ID!");
                return;
            }
            if (kiemtra(txtProName.getText(), 1)) {
                JOptionPane.showMessageDialog(rootPane, "Project name already exists, please insert other name!");
                return;
            }
            byte x = Byte.valueOf(txtProStatus.getText());
            Project pro_in = new Project(txtProID.getText(), txtProName.getText(), txtProStart.getText(), txtProFinish.getText(), x);
            //            daoPro.insertProject(pro_in);
            int check = daoPro.insertProject(pro_in);

            if (check == 1) {
                hienthi(pro_in);
                xoaTxt();

                JOptionPane.showMessageDialog(rootPane, "Add project success!");
            } else {
                JOptionPane.showMessageDialog(rootPane, "Wrong valid of Project Start or Project Finish!");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Wrong valid of Status! If finish insert 1 else insert 0! ");
            return;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

