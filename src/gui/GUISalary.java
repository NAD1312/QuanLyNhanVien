    public void printData(Vector<Salary> list) {
        // Show list through for-each
        for (Salary item : list) {
            System.out.println(item.getSalary_id());
        }
        System.out.println();
    }

    public GUISalary() {
        Vector<Salary> sa = new Vector<Salary>();
        sa = daoSa.getAllSa();
        initComponents();
        printData(sa);
        if (sa.size() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Không có mức lương nào để hiển thị!");
        } else {
            int index;
            for (index = 0; index < sa.size(); index++) {
                Vector vctRow = new Vector();
                vctRow.add(sa.elementAt(index).getSalary_id());
                vctRow.add(sa.elementAt(index).getSalary_base());
                vctData.add(vctRow);
            }
        }
        vctHeader.add("Mã lương");
        vctHeader.add("Số tiền");
        SalaryTable.setModel(new DefaultTableModel(vctData, vctHeader));
        System.out.println("done!");
    }

