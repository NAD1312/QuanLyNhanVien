    public String nhap_tien_luong(String y) {
        String x = new String();
        x = JOptionPane.showInputDialog("Nhập " + y + " tiền lương mới:").toString();
        boolean flag = false;
        while (flag == false) {
            try {
                int nam = Integer.parseInt(x);
                System.out.println(nam);
                flag = true;
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Bạn nhập " + y + " tiền lương mới sai định dạng!");
            }
            if (flag == false) {
                x = JOptionPane.showInputDialog("Nhập " + y + " dự án mới:").toString();
            } else {
                break;
            }
        }
        return x;
    }

    public void xoaTxt() {
        txtSaID.setText("");
        txtSaBase.setText("");
        txtSaID.requestFocusInWindow();
    }

    public void hienthi(Salary x) {
        Vector vctRow = new Vector();
        vctRow.add(x.getSalary_id());
        vctRow.add(x.getSalary_base());
        vctData.add(vctRow);
        SalaryTable.setModel(new DefaultTableModel(vctData, vctHeader));
    }

    public void them(Salary x, int vitri) {
        Vector vctRow = new Vector();
        vctRow.add(x.getSalary_id());
        vctRow.add(x.getSalary_base());
        vctData.set(vitri, vctRow);
        SalaryTable.setModel(new DefaultTableModel(vctData, vctHeader));
    }

    public void xoa(int vitri) {
        vctData.removeElementAt(vitri);
        SalaryTable.setModel(new DefaultTableModel(vctData, vctHeader));
    }

