    public int kiemtra_ma(String x) {
        int vitri = -1;
        for (int i = 0; i < vctData.size(); i++) {
            Vector temp = new Vector((Vector) vctData.elementAt(i));
            if (x.equals(temp.get(0))) {
                vitri = i;
                return vitri;
            }
        }
        return vitri;
    }
