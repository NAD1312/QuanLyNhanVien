    private void btnXoaSaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaSaActionPerformed
        // TODO add your handling code here:
        String xoa_pro = JOptionPane.showInputDialog("Insert Salary ID need to delete:").toString();
        System.out.println(xoa_pro);
        int i = 0;
        int vitri = 0;
        boolean flag = false;
        for (i = 0; i < vctData.size(); i++) {
            Vector temp = new Vector((Vector) vctData.elementAt(i));
            if (xoa_pro.equals(temp.get(0))) {
                flag = true;
                vitri = i;
            }
        }
        if (flag == true) {
            int check = daoSa.deleteSalary(xoa_pro);
            if (check == 0) {
                JOptionPane.showMessageDialog(rootPane, "Delete failed, there is atleast one staff in this salary!");
            } else {
                System.out.println(vitri);
                xoa(vitri);
                JOptionPane.showMessageDialog(rootPane, "Delete success!");

            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Salary ID is not exists!");
        }
    }//GEN-LAST:event_btnXoaSaActionPerformed

    private void btnSuaSaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaSaActionPerformed
        // TODO add your handling code here:
        String sua_sa_id = txtSaID.getText().toString();
        while (sua_sa_id.length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Salary ID is empty!");
            return;
        }
        int vitri = kiemtra_ma(sua_sa_id);
        if (vitri != -1) {
            String sua_sa_base = new String();

            sua_sa_base = txtSaBase.getText().toString();
            while (sua_sa_base.length() == 0) {
                JOptionPane.showMessageDialog(rootPane, "Salary Base is empty!");
                return;
            }
            try {
                int luong = Integer.parseInt(sua_sa_base);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Wrong valid of Salary Base!");
                return;
            }
            Salary sua_sa = new Salary(sua_sa_id, Integer.parseInt(sua_sa_base));
            daoSa.ChangeSalary(sua_sa);
            them(sua_sa, vitri);
            JOptionPane.showMessageDialog(rootPane, "Fix success!");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Salary ID is not exists!");
            return;
        }
    }//GEN-LAST:event_btnSuaSaActionPerformed

